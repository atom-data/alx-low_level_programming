#include "main.h"

/**
 * add - sum of two integers
 * @a: first input
 * @b: second input
 *
 * Return: Always an integer
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - difference between two integers
 * @a: first input
 * @b: second input
 *
 * Return: Always an integer
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - product of two integers
 * @a: first input
 * @b: second input
 *
 * Return: Always an integer
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - remainder
 * @a: number
 * @b: divisor
 *
 * Return: Always an integer
 */

int mod(int a, int b)
{
	return (a % b);
}

/**
 * div - quotient
 * @a: number
 * @b: divisor
 *
 * Return: Always an integer
 */
int div(int a, int b)
{
	return (a / b);
}
